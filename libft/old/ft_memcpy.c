#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t		i;
	t_uchar		*tmp_dst;
	t_uchar		*tmp_src;

	tmp_dst = (t_uchar*)dst;
	tmp_src = (t_uchar*)src;
	i = 0;
	while (i < n)
	{
		tmp_dst[i] = tmp_src[i];
		i++;
	}
	return (dst);
}
